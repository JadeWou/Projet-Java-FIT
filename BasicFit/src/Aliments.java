
public class Aliments {
	public Aliments(String libelle, int kcal, double matires_grasses, double acide_gras_sature, double glucides,
			double sucres, double fibres_alimentaires, double proteines, double sel) {
		super();
		this.libelle = libelle;
		this.kcal = kcal;
		this.matires_grasses = matires_grasses;
		this.acide_gras_sature = acide_gras_sature;
		this.glucides = glucides;
		this.sucres = sucres;
		this.fibres_alimentaires = fibres_alimentaires;
		this.proteines = proteines;
		this.sel = sel;
	}
	String libelle;
	int kcal;
	double matires_grasses;
	double acide_gras_sature;
	double glucides;
	double sucres;
	double fibres_alimentaires;
	double proteines;
	double sel;
}
